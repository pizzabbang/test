package sample1;

public class Point {
	private double xpos; // x좌표
	private double ypos; // y좌표
	
	public double getXpos() {
		return xpos;
	}
	public void setXpos(double xpos) { // 주입
		this.xpos = xpos;
	}
	public double getYpos() {
		return ypos;
	}
	public void setYpos(double ypos) {
		this.ypos = ypos;
	}
	
	
}
